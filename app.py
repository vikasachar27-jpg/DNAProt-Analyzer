# 2. FILE PROCESSING LOGIC
def analyze_sequence(file_obj):
    if file_obj is None:
        return "⚠️ Please upload a FASTA file.", None, "0 bp", "0.00 %", "0 Da"

    script_path = "./DNAProt.sh"

    try:
        # Run the Bash script
        result = subprocess.run(
            ['bash', script_path, file_obj.name], 
            capture_output=True, 
            text=True
        )

        if result.stderr:
            return f"❌ Script Error:\n{result.stderr}", None, "Error", "Error", "Error"

        report_text = result.stdout
        
        # --- PARSING BASIC METRICS FOR VISUAL BADGES ---
        seq_len = "0 bp"
        gc_cont = "0.00 %"
        mol_wt = "0 kDa"
        
        for line in report_text.split('\n'):
            if "Sequence Length" in line or "Sequence Info" in line:
                try: seq_len = line.split(':')[1].split('|')[0].strip()
                except: pass
            if "GC Content" in line:
                try: gc_cont = line.split(':')[1].strip()
                except: pass
            if "Est. Mol. Weight" in line:
                try: mol_wt = line.split(':')[1].strip()
                except: pass

        # --- GENERATE PDF REPORT ---
        pdf_filename = "DNAProt_Analysis_Report.pdf"
        doc = SimpleDocTemplate(pdf_filename, pagesize=letter, rightMargin=40, leftMargin=40, topMargin=40, bottomMargin=40)
        story = []
        
        styles = getSampleStyleSheet()
        title_style = ParagraphStyle(
            'TitleStyle', parent=styles['Heading1'], fontSize=20, textColor=colors.HexColor('#1e3a8a'), spaceAfter=15, alignment=1
        )
        body_style = ParagraphStyle(
            'BodyStyle', parent=styles['Code'], fontSize=10, leading=14, textColor=colors.HexColor('#1f2937')
        )
        
        story.append(Paragraph("🧬 DNAProt Pro Analyzer - Report", title_style))
        story.append(Paragraph("Generated by Team: Adisri Menon, Dhanush R M, Vikas K R", styles['Normal']))
        story.append(Spacer(1, 15))
        
        for line in report_text.split('\n'):
            clean_line = line.replace(' ', '&nbsp;').replace('=', '=').replace('-', '-')
            story.append(Paragraph(clean_line if clean_line else "<br/>", body_style))
            
        doc.build(story)

        return report_text, pdf_filename, seq_len, gc_cont, mol_wt

    except Exception as e:
        return f"❌ System Error: {str(e)}", None, "Error", "Error", "Error"

